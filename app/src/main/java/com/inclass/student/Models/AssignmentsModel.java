package com.inclass.student.Models;

public class AssignmentsModel {

    private String content_title;
    private String content_type;
    private String content_desription;
    private String content_file;
    private String updatedate;

    public String getContent_title() {
        return content_title;
    }

    public void setContent_title(String content_title) {
        this.content_title = content_title;
    }

    public String getContent_type() {
        return content_type;
    }

    public void setContent_type(String content_type) {
        this.content_type = content_type;
    }

    public String getContent_desription() {
        return content_desription;
    }

    public void setContent_desription(String content_desription) {
        this.content_desription = content_desription;
    }

    public String getContent_file() {
        return content_file;
    }

    public void setContent_file(String content_file) {
        this.content_file = content_file;
    }


    public String getUpdatedate() {
        return updatedate;
    }

    public void setUpdatedate(String updatedate) {
        this.updatedate = updatedate;
    }


}
